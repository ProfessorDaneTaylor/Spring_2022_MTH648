# two functions for HW1

def sixth_power(x):
	"""
    Jie's first function.

    Args:
        x (float): some floating number.

    Returns:
        float: Returns :math:`y=x^6`
    """

	return x**6

def multiply_by_6(a):
	"""
    Jie's second function.

    Args:
        a (float): some floating number.

    Returns:
        float: Returns :math:`b=6a`
    """

	return 6*a


